<h3 class="h3">38) What do you understand by Angular MVVM architecture? </h3>
<p>The MVVM architecture or <strong>Model-View-ViewModel</strong> architecture is a software architectural pattern that provides a facility to developers to separate the development of the graphical user interface (the View) from the development of the business logic or back-end logic (the Model). By using this architecture, the view is not dependent on any specific model platform.</p>
<p>The Angular MVVM architecture consists of the following three parts:</p>
<ul class="points">
<li>Model</li>
<li>View</li>
<li>ViewModel</li>
</ul>
<img src="https://static.javatpoint.com/interview/images/top-angular-interview-questions-q38.png" alt="Angular Interview questions" />
<p><strong>Model:</strong> The Model consists of the structure of an entity and specifies the approach. In simple words, we can say that the model contains data of an object.</p>
<p><strong>View:</strong> The View is the visual layer of the application. It specifies the structure, layout, and appearance of what a user sees on the screen. It displays the data inside the Model, represents the model, and receives the user's interaction with the view in the form of mouse clicks, keyboard input, screen tap gestures, etc., and forwards these to the ViewModel via the data binding properties. In Angular terms, the View contains the HTML template of a component.</p>
<p><strong>ViewModel:</strong> The ViewModel is an abstract layer of the application. It is used to handle the logic of the application. It also manages the data of a model and displays it in the view. View and ViewModel are connected with two-way data-binding. If you make any changes in the view, the ViewModel takes a note and changes the appropriate data inside the model.</p>
