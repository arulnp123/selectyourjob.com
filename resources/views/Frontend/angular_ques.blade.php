<h3 class="h3">13) What is AOT in Angular?</h3>
<p>In Angular, AOT stands for Ahead-Of-Time compiler. It is used to convert your Angular HTML and TypeScript code into efficient JavaScript code during the build phase before the browser downloads and runs that code. By compiling the application during the build process provides a faster rendering in the browser. </p>
<hr/>
<h3 class="h3">14) What is the reason for using the AOT compiler in Angular?</h3>
<p>An Angular application is made of several components and their HTML templates. Because of these Angular components and templates, the browsers are not able to understand them directly. So, Angular applications require a compilation process before they run in a browser. That's why AOT compilers are required.</p>
<hr/>
<h3 class="h3">15) What are the biggest advantages of AOT in Angular?</h3>
<p>Following are the advantages of using the AOT compiler in Angular:</p>
<p><strong>The rendering is faster:</strong> When we use the AOT compiler, the browser gets a pre-compiled version of the application to download. Here, the browser loads executable code to render the application immediately, without waiting to compile the app first.</p>
<p><strong>The Angular framework's download size is smaller:</strong> AOT facilitates you not to download the Angular compiler if the app is already compiled. The compiler is roughly half of Angular itself, so omitting it dramatically reduces the application payload.</p>
<p><strong>Fewer asynchronous requests:</strong> The compiler is used to inline external HTML templates and CSS style sheets within the application JavaScript so, it eliminates separate AJAX requests for those source files.</p>
<p><strong>Detect template errors earlier:</strong> While using the AOT compiler, developers can easily detect and report template binding errors during the build step before users can see them.</p>
<p><strong>Better security:</strong> AOT provides better security because it compiles HTML templates and components into JavaScript files before they are served to the client. Because there are no templates to read and no risky client-side HTML or JavaScript evaluation, so the chances for injection attacks are very rare.</p>
