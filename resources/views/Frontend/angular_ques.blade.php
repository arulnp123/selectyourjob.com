<h3 class="h3">16) What is JIT in Angular?</h3>
<p>In Angular, JIT stands for Just-in-Time compiler. The JIT compiler provides a dynamic translation or run-time compilation, which provides a way of executing computer code that involves compilation during the execution of a program at run time rather than before execution. </p>
