<p><strong>ngOnChanges( ):</strong> This method is called when one or more input properties of the component are changed. The hook receives a SimpleChanges object containing the previous and current values of the property.</p>
<p><strong>ngOnInit( ):</strong> This is the second lifecycle hook. It is called once, after the ngOnChanges hook. It is used to initialize the component and sets the input properties of the component.</p>
<p><strong>ngDoCheck( ):</strong> This hook is called after ngOnChanges and ngOnInit and is used to detect and act on changes that Angular cannot detect. In this hook, we can implement our change detection algorithm.</p>
<p><strong>ngAfterContentInit( ):</strong> This hook is called after the first ngDoCheck hook. This hook responds after the content gets projected inside the component.</p>
<p><strong>ngAfterContentChecked( ):</strong> This hook is called after ngAfterContentInit and every subsequent ngDoCheck. It responds after the projected content is checked.</p>
<p><strong>ngAfterViewInit( ):</strong> This hook is called after a component's view or initializing a child component's view.</p>
<p><strong>ngAfterViewChecked( ):</strong> This hook is called after ngAfterViewInit. It responds after the component's view or when the child component's view is checked.</p>
<p><strong>ngOnDestroy( ):</strong> This hook is called just before Angular destroys the component. This is used to clean up the code and detach event handlers.</p>
