			<h3 class="h3">34) What is the difference between the MVVM Cross and MVVM Light?</h3>
			<p><strong>MVVM Cross:</strong> MVVM Cross is a .NET cross-platform MVVM framework. It allows us to make a cross-platform solution for platforms such as Xamarin. Forms, Xamarin.Android, Xamarin.iOS, Xamarin.Mac and WPF. It is an inactive state of development. </p>
			<p>MVVM Cross requires the application to be divided into two parts: Core and the UI.</p>
			<p>The Core part contains the View Models, Service, Models, and the Business logic, whereas the UI part consists of the different views and platform-specific code that interact with the core. The views are the View Screens, which contain the graphical content. In addition to Core and UI, the application may contain additional libraries for various functionalities. </p>
			<p><strong>Features that are provided by MVVM Cross are: </strong></p>
			<ul class="points">
			<li>An MVVM architecture pattern</li>
			<li>A flexible Navigation system</li>
			<li>Data binding to allow automatic binding with the source to target.</li>
			<li>Platform-specific support</li>
			<li>Inversion of Control Container</li>
			<li>Dependency Injection engine</li>
			</ul>
			<p><strong>Consequences of MVVM Cross are: </strong></p>
			<p>It has an excessive learning curve and lacks a Wiki for how to get started developing the apps.</p>
			<p><strong>MVVM Light:</strong> MVVM Light is another Framework that allows the creation of <strong>Enterprise-Grade</strong> apps using the <strong>MVVM Architectural</strong> pattern on Xamarin. MVVM Light enables the developer to create and develop MVVM applications on a variety of platforms like Xamarin. Forms, Xamarin.Android, Xamarin.iOS, UWP, and WPF. It allows us to separate the View from Model and hence write the testable and extensible applications. It does not support async.</p>
			<p><strong>MVVM Light application is divided into the following parts:</strong></p>
			<ul class="points">
			<li><strong>View:</strong> It is a platform-specific user interface which is associated with the screen.</li>
			<li><strong>ViewModel:</strong> It exposes the data and related properties of the data.</li>
			<li><strong>ViewModel Locator:</strong> It registers custom ViewModels.</li>
