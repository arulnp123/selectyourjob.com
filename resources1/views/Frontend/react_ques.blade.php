			 constructor(props){
			   super(props);
			   this.state = {
				 counterValue: 0
			   }
			   this.incrementCounter = this.incrementCounter.bind(this);
			 }
			 incrementCounter(){
			   this.setState(prevState =&gt; counterValue = prevState+1);
			 }
			 render(){
			   if(this.state.counter === 2){
				 throw new Error('Crashed');
			   }
			   return(
				 &lt;div&gt;
				   &lt;button onClick={this.incrementCounter}&gt;Increment Value&lt;/button&gt;
				   &lt;p&gt;Value of counter: {this.state.counterValue}&lt;/p&gt;
				 &lt;/div&gt;
			   )
			 }
			}
			</textarea></div>
			<p>In the above code, you can see that when the counterValue equals 2, it throws an error inside the render method. We know that any error inside the render method leads to unmounting of the component so, to display an error that occurs inside the render method, we use error boundaries. When we are not using the error boundary, we see a blank page instead of seeing an error.</p>
			<p>With error boundaries:</p>
			<p>We have specified earlier that error boundary is a component using one or both of the following methods:</p>
			<ul class="points">
