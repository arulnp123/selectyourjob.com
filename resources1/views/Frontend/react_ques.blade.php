			   super(props);
			   this.state = { hasError: false };
			 }
			 static getDerivedStateFromError(error) {     
			   return { hasError: true }; 
			 }
			  componentDidCatch(error, errorInfo) {       
			   logErrorToMyService(error, errorInfo); 
			 }
			 render() {
			   if (this.state.hasError) {     
				 return &lt;h4&gt;Something went wrong&lt;/h4&gt;     
			   }
			   return this.props.children;
			 }
			}
			</textarea></div>
			<p>You can see in the above code the getDerivedStateFromError function renders the fallback UI interface when the render method has an error.</p>
