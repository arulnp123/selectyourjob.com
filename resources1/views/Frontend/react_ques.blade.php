			<h3 class="h3">30) What are the different phases of React component's lifecycle?</h3>
			<p>The different phases of React component's lifecycle are:</p>
			<p><strong>Initial Phase:</strong> It is the birth phase of the React lifecycle when the component starts its journey on a way to the DOM. In this phase, a component contains the default Props and initial State. These default properties are done in the constructor of a component.</p>
			<p><strong>Mounting Phase:</strong> In this phase, the instance of a component is created and added into the DOM.</p>
			<p><strong>Updating Phase:</strong> It is the next phase of the React lifecycle. In this phase, we get new Props and change State. This phase can potentially update and re-render only when a prop or state change occurs. The main aim of this phase is to ensure that the component is displaying the latest version of itself. This phase repeats again and again.</p>
			<p><strong>Unmounting Phase:</strong> It is the final phase of the React lifecycle, where the component instance is destroyed and unmounted(removed) from the DOM.</p>
			<p>For More Information, <a href="react-component-life-cycle"><span class="bold"><em> </em></span></a>.</p>
