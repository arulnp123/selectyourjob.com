			<h3 class="h3">66) What is an error boundary or error boundaries?</h3>
			<p>An error boundary is a concept introduced in version 16 of React. Error boundaries provide a way to find out the errors that occur in the render phase. Any component which uses one of the following lifecycle methods is considered an error boundary. Let's see the places where an error boundary can detect an error:</p>
			<ul class="points">
			<li>Render phase</li>
			<li>Inside a lifecycle method</li>
			<li>Inside the constructor</li>
			</ul>
