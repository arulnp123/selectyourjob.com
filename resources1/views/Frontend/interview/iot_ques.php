						<p class="qus">4) What are the fundamental components of IoT?</p>
						<p class="ans"><img src="<?= base_url(); ?>assets/img/iot.gif" style="width:300px;" /><br>
						The four fundamental components of an IoT system are:<br>
<ul>
<li>●	Sensors/Devices: Sensors or devices are a key component that helps you to collect live data from the surrounding environment. All this data may have various levels of complexities. It could be a simple temperature monitoring sensor, or it may be in the form of the video feed.</li>
<li>●	Connectivity: All the collected data is sent to a cloud infrastructure. The sensors should be connected to the cloud using various mediums of communications. These communication mediums include mobile or satellite networks, Bluetooth, WI-FI, WAN, etc.</li>
<li>●	Data Processing: Once that data is collected, and it gets to the cloud, the software product performs processing on the gathered data. This process can be just checking the temperature, reading on devices like AC or heaters. However, it can sometimes also be very complex, like identifying objects, using computer vision on video.</li>
<li>●	User Interface: The information needs to be available to the end-user in some way, which can be achieved by triggering alarms on their phones or sending them notification through email or text message. The user sometimes might need an interface which actively checks their IoT system.</li>
</ul>
