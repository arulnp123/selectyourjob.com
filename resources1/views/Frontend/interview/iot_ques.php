						<p class="qus">1. What is the Internet Of Things (IoT)?</p>
						<p class="ans">Internet of Things (IoT) is a network of physical objects or people called “things” that are embedded with software, electronics, network, and sensors that allow these objects to collect and exchange data. The goal of IoT is to extend to internet connectivity from standard devices like computer, mobile, tablet to relatively dumb devices like a toaster.</p>
                      
